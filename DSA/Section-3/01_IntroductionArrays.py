# Types of arrays : One Dimensional or Multi Dimensional(2-d , 3-d , 4-d)



# One-D Array : An array with a bunch of values having been declared with a single index
# For Example : a[i] -> i between 0 to n
# [1,2,3,4,5,6,7,.....n]


# Two-D Array : An array with a bunch of values having been declared with a double index
# For Example : a[i][j] -> i and j between 0 to n

# i being the row , and j being the column index
# [1,2,3,4,5,6,7,............n]
# [5,6,7,8,9,0,11,...........n]
# [12,23,34,45,56,67,78,.....n]

# here in the above example , a[0][4] = 4


# Three-D Array : An array with a bunch of values having been declared with a double index
# For Example : a[i][j] -> i and j and k between 0 to n

# i being the row , and j being the column index
# [ 1  2  3 ]  [ 7  8  9 ]  
# [ 4  5  6 ]  [10 11 12 ] 

# here in the above example , a[0][4] = 4



